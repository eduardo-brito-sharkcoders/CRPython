# Conjunto de "variáveis"
lista_alimentos = ["chocolate", "laranja", "morango", "baunilha", "peixe", "limão"]

# Ciclo for
for alimento in lista_alimentos:
    print(alimento)

# Alterar elemento 0 da lista
lista_alimentos[0] = "gelatina"

print("\n")

for alimento in lista_alimentos:
    print(alimento)

